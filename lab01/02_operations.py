def solve_25():
    """Возвращает выражение, дающее 25 из чисел 1 2 3 4 5"""
    return "1*(2+3)*4+5"

def calculate():
    """Вычисляет значение выражения"""
    return 1 * (2 + 3) * 4 + 5

def check(expr, target=25):
    """Проверяет, даёт ли выражение нужный результат"""
    return eval(expr) == target
